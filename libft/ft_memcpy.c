void	*ft_memcpy(void *dst, const void *src, int n)
{
	int i;

	i = 0;
	if (dst == src)
		return (dst);
	while (i < n)
	{
		((char*)dst)[i] = ((char*)src)[i];
		i++;
	}
	return (dst);
}
